#include <stdio.h>
/**
 * add - A function that adds two numbers
 * @a: first number
 * @b: second number
 * Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * add - A function that subtract two numbers
 * @a: first number
 * @b: second number
 * Return: a - b
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * add - A function that multiply two numbers
 * @a: first number
 * @b: second number
 * Return: a * b
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * add - A function that divide two numbers
 * @a: first number
 * @b: second number
 * Return: a / b
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * add - A function mod two numbers
 * @a: first number
 * @b: second number
 * Return: a % b
 */
int mod(int a, int b)
{
	return (a % b);
}

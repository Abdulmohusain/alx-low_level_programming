/**
 * op_add - A function that find the sum of two numbers.
 * @a: number 1
 * @b: Number 2
 * Return: Sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - A function that the difference between numbers.
 * @a: number 1
 * @b: Number 2
 * Return: Difference between a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - A function that multiplies two numbers.
 * @a: number 1
 * @b: Number 2
 * Return: The product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - A function that divides @a by @b.
 * @a: number 1
 * @b: Number 2
 * Return: Value of a divided by b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - A function that find the modulus of @a by @b.
 * @a: number 1
 * @b: Number 2
 * Return: Remainder of division of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
